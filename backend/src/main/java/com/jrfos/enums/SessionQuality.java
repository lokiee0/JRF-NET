package com.jrfos.enums;

/**
 * Represents the subjective quality of a study session.
 * Ordered from worst to best.
 */
public enum SessionQuality {
    POOR,    // Very distracted, barely focused
    LOW,     // Below average focus
    MEDIUM,  // Average session
    HIGH,    // Good focus and progress
    PEAK     // Deep work, excellent retention
}
